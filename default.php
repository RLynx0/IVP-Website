                    function listFiles($filePath, $prevsPath, $isSub)
                    {
                        $prevs = scandir($prevsPath);
                        $documents = scandir($filePath);
                        if ($key = array_search(".gitkeep", $documents)) {
                            unset($documents[$key]);
                        }

                        if (count($documents) <= 2) {
                            if ($isSub) {
                                print "<p href='" . $filePath . "/noCont' class='hide'>";
                                print "Keine Dokumente in Ordner</p>";
                            } else {
                                print "<p>Derzeit keine Dokumente verfügbar</p>";
                            }
                            return;
                        };

                        for ($i = 2; $i < count($documents); $i++) {
                            $path = $documents[$i];

                            if (is_dir($filePath . "/" . $path)) {
                                print "<button ";
                                if ($isSub) print "class='hide'";
                                print "onclick='openDir(this)' href='" . $filePath . "/" . $path . "' open='false'>";
                                print "<img src='./img/prev/closedFolderPrev.svg' alt='Preview for Folder " . $path . "'>";
                                print "<h3>" . $path . "</h3>";
                                print "</button>";

                                listFiles($filePath . "/" . $path, $prevsPath, true);

                                // print "<h3 href='" . $filePath . "/" . $path . "/endCont' class='hide'>";
                                // print $path . "&gt;";
                                // print "</h3>";
                                continue;
                            }

                            $name = explode(".", $path)[0];
                            $type = explode(".", $path)[1];
                            if (!in_array($type . "Prev.svg", $prevs)) {
                                $name = $name . "." . $type;
                                $type = "default";
                            }

                            print "<a ";
                            if ($isSub) print "class='hide'";
                            print "href='" . $filePath . "/" . $path . "' target='empty'>";
                            print "<img src='./img/prev/" . $type . "Prev.svg' alt='Preview for " . $name . "'>";
                            print "<h3>" . $name . "</h3>";
                            print "</a>";
                        }
                    }

